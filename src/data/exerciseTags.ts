// Helper functions for filtering exercises by tags
export const getExerciseTag = (exerciseId: string): ExerciseTag | undefined => {
  return exerciseTags.find((tag) => tag.exerciseId === exerciseId);
};

export const filterByPositionRelevance = (
  position: 'front' | 'mid' | 'back',
  minScore: number = 3
): ExerciseTag[] => {
  return exerciseTags.filter((tag) => tag.positionRelevance[position] >= minScore);
};

export const filterBySideBiasRelevance = (
  sideBias: 'snake' | 'dorito',
  minScore: number = 3
): ExerciseTag[] => {
  return exerciseTags.filter((tag) => tag.sideBiasRelevance[sideBias] >= minScore);
};

export const filterByComplexity = (maxLevel: ComplexityLevel): ExerciseTag[] => {
  return exerciseTags.filter((tag) => tag.complexityLevel <= maxLevel);
};

export const filterByMovementPattern = (pattern: MovementPattern): ExerciseTag[] => {
  return exerciseTags.filter((tag) => tag.movementPattern === pattern);
};

export const filterByEnergySystem = (system: EnergySystem): ExerciseTag[] => {
  return exerciseTags.filter((tag) => tag.energySystem === system);
};

export const filterByPlaneOfMotion = (plane: PlaneOfMotion): ExerciseTag[] => {
  return exerciseTags.filter((tag) => tag.planeOfMotion === plane);
};

// Combined filter for workout assembly
export const filterExercisesForPlayer = (
  position: 'front' | 'mid' | 'back',
  sideBias: 'snake' | 'dorito' | 'both',
  complexityLevel: ComplexityLevel,
  minPositionScore: number = 3,
  minSideBiasScore: number = 3
): ExerciseTag[] => {
  return exerciseTags.filter((tag) => {
    // Check complexity
    if (tag.complexityLevel > complexityLevel) return false;

    // Check position relevance
    if (tag.positionRelevance[position] < minPositionScore) return false;

    // Check side bias relevance (for 'both', check either side meets threshold)
    if (sideBias === 'both') {
      if (tag.sideBiasRelevance.snake < minSideBiasScore &&
          tag.sideBiasRelevance.dorito < minSideBiasScore) {
        return false;
      }
    } else {
      if (tag.sideBiasRelevance[sideBias] < minSideBiasScore) return false;
    }

    return true;
  });
};

// Score an exercise for a specific player profile
export const scoreExerciseForPlayer = (
  tag: ExerciseTag,
  position: 'front' | 'mid' | 'back',
  sideBias: 'snake' | 'dorito' | 'both'
): number => {
  let score = tag.positionRelevance[position];

  if (sideBias === 'both') {
    // Average of both sides
    score += (tag.sideBiasRelevance.snake + tag.sideBiasRelevance.dorito) / 2;
  } else {
    score += tag.sideBiasRelevance[sideBias];
  }

  return score;
};
